     public void loadToTable(){
        DefaultTableModel dtm =  (DefaultTableModel) tblSell.getModel();
        dtm.setRowCount(0);
        
        List<sell> list = sellController.list();
        for(sell s1 :list){
            Object[] row = {s1.getId(),s1.getName(),s1.getAmount(),s1.getPaymentTotal(),s1.getBalance(),s1.getTargetDate(),s1.isStatus()};
            dtm.addRow(row);
            
        }
         
    }   

    

    
