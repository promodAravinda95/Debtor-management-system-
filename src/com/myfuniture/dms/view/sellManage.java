    private void tblsellMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblsellMouseClicked
 
     
        try{
            int row = tblSell.getSelectedRow();
            Integer id = (Integer)tblSell.getValueAt(row,0);
            sell sm1 = sellController.get(id);
            txtName.setText(sm1.getName());
            txtAmount.setText(sm1.getAmount() + "");
            Date targetDate = sm1.getTargetDate();
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-mm-dd");
            String targetDateS = sdf.format(targetDate);
            txtDate.setText(targetDateS);
          
          
        }catch(Exception e){
            
            e.printStackTrace();
        }
          


    }//GEN-LAST:event_tblsellMouseClicked

